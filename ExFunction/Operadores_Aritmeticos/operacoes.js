//Soma
export function soma(a, b) {
  return a + b;
}

//Subtração
export function subtracao(a, b) {
  return a - b;
}

//Multiplicação
export function multiplicacao(a, b) {
  return a * b;
}

//Divisão
export function divisao(a, b) {
  return a / b;
}

//Resto
export function resto(a, b) {
  return a % b;
}

//Potenciação
export function potenciacao(a, b) {
  return a ** b;
}

//Raiz quadrada
export function raizQuadrada(a) {
  return Math.sqrt(a);
}