export function maiorQue(x, y) {
    return x > y;
}

export function menorQue(x, y) {
    return x < y;
}

export function maiorOuIgualA(x, y) {
    return x >= y;
}

export function menorOuIgualA(x, y) {
    return x <= y;
}

export function igualA(x, y) {
    return x === y;
}

export function diferenteDe(x, y) {
    return x !== y;
}